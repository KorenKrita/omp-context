#!/usr/bin/env bun
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = join(repoRoot, "skills", "context-management", "CORE.md");
const outputPath = join(repoRoot, "src", "generated-guidance.ts");
const source = readFileSync(sourcePath, "utf8");

function extract(name) {
  const startMarker = `<!-- ACM:${name}:START -->`;
  const endMarker = `<!-- ACM:${name}:END -->`;
  const starts = source.split(startMarker).length - 1;
  const ends = source.split(endMarker).length - 1;
  if (starts !== 1 || ends !== 1) throw new Error(`Expected exactly one ${name} marker pair; found ${starts}/${ends}`);
  const start = source.indexOf(startMarker) + startMarker.length;
  const end = source.indexOf(endMarker, start);
  return source.slice(start, end).trim();
}

const sections = {
  core: extract("CORE"),
  checkpoint: extract("TOOL_CHECKPOINT"),
  timeline: extract("TOOL_TIMELINE"),
  travel: extract("TOOL_TRAVEL"),
  checkpointStart: extract("CUE_CHECKPOINT_START"),
  checkpointDone: extract("CUE_CHECKPOINT_DONE"),
  travelPhase: extract("CUE_TRAVEL_PHASE"),
  travelTask: extract("CUE_TRAVEL_TASK"),
  nameCollision: extract("RECOVERY_NAME_COLLISION"),
  hostCapability: extract("RECOVERY_HOST_CAPABILITY"),
  rollbackFailed: extract("RECOVERY_ROLLBACK_FAILED"),
  refreshExhausted: extract("RECOVERY_REFRESH_EXHAUSTED"),
};

const generated = `// Generated by scripts/generate-guidance.mjs from skills/context-management/CORE.md.\n// Do not edit directly.\n\nexport const ACM_CORE_MARKER = "<!-- OMP-CONTEXT:ACM-CORE:v1 -->";\n\nexport const ACM_CORE = ${JSON.stringify(sections.core)};\n\nexport const TOOL_DESCRIPTIONS = ${JSON.stringify({
  checkpoint: sections.checkpoint,
  timeline: sections.timeline,
  travel: sections.travel,
}, null, 2)} as const;\n\nexport const GUIDANCE_CUES = ${JSON.stringify({
  checkpointStart: sections.checkpointStart,
  checkpointDone: sections.checkpointDone,
  travelPhase: sections.travelPhase,
  travelTask: sections.travelTask,
}, null, 2)} as const;\n\nexport const RECOVERY_GUIDANCE = ${JSON.stringify({
  nameCollision: sections.nameCollision,
  hostCapability: sections.hostCapability,
  rollbackFailed: sections.rollbackFailed,
  refreshExhausted: sections.refreshExhausted,
}, null, 2)} as const;\n`;

writeFileSync(outputPath, generated);
process.stdout.write(`Generated ${outputPath}\n`);
